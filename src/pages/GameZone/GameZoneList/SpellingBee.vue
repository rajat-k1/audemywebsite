 <script setup>
import { onMounted, onUnmounted, ref, watch, computed } from "vue";
import GamePagesHeader from '../../Header/GamePagesHeader.vue';
import { requestMicPermission } from "../../../Utilities/requestMicAccess";
import {
playIntro,
playQuestion,
playSound,
stopAudios,
playScore,
} from "../../../Utilities/playAudio";
import {
startListening,
stopListening,
} from "../../../Utilities/speechRecognition";

// Device detection
const isTablet = ref(false);
const isMobile = ref(false);

// Check device type on mount and on window resize
const checkDeviceType = () => {
const width = window.innerWidth;
if (width >= 640 && width < 768) {
  // Small devices (large phones)
  isTablet.value = false;
  isMobile.value = true;
} else if (width >= 768 && width < 1024) {
  // Medium devices (tablets)
  isTablet.value = true;
  isMobile.value = false;
} else if (width >= 1024) {
  // Large devices (laptops/desktops)
  isTablet.value = false;
  isMobile.value = false;
} else {
  // Extra small devices (phones)
  isTablet.value = false;
  isMobile.value = true;
}
};

const currentAudios = [],
randQueNum = [];
let numOfAudiosPlayed = ref(0),
score = ref(0);
let questionsDb = [],
isListening = ref(false),
transcription = ref(""),
playButton = ref(false),
isIntroPlaying = ref(false),
isRecording = ref(false);

// Add new ref for tracking if a question is currently playing
const isQuestionPlaying = ref(false);

// Add a ref to track if the repeat button is in cooldown
const isRepeatButtonCooldown = ref(false);

// Generate multiplication questions using Json file
const generateQuestions = () => {
console.log("Generating Questions...");
// Generate 5 random numbers for the questions
while (randQueNum.length < 5) {
  let num = Math.floor(Math.random() * 15);
  if (!randQueNum.includes(num)) {
    randQueNum.push(num);
  }
}
// Fetch questions from JSON file
fetch("/assets/questionsDb/spellingBeeDB.json")
  .then((response) => response.json())
  .then((data) => {
    console.log(
      "Questions:",
      data["SpellingBeeGame"]["Questions"]["Easy"]
    );
    // Process the questions data as needed
    questionsDb = data["SpellingBeeGame"]["Questions"]["Easy"];
  })
  .catch((error) => {
    console.error("Error fetching questions:", error);
  });
};

// Play the next question
const playNextQuestion = () => {
if (numOfAudiosPlayed.value < 5) {
  const question = questionsDb[randQueNum[numOfAudiosPlayed.value]];
  console.log(question);
  isQuestionPlaying.value = true;
  const audio = playQuestion(question["Q"]);
  currentAudios.push(audio);
  
  // Set isQuestionPlaying to false when audio ends
  audio.onended = () => {
    isQuestionPlaying.value = false;
  };
  
  // Backup timeout in case onended doesn't fire
  const estimatedDuration = question["Q"].length * 100 + 2000; // Add 2 seconds buffer
  setTimeout(() => {
    if (isQuestionPlaying.value) {
      isQuestionPlaying.value = false;
    }
  }, estimatedDuration);
}
};

// Add a function for the repeat button with debounce
const repeatQuestion = () => {
// Prevent rapid clicks by checking cooldown
if (isRepeatButtonCooldown.value) {
  return;
}

// Only allow repeating if intro has finished and no question is currently playing
if (!isIntroPlaying.value && !isQuestionPlaying.value && numOfAudiosPlayed.value < 5) {
  // Set cooldown flag to prevent rapid clicks
  isRepeatButtonCooldown.value = true;
  
  const question = questionsDb[randQueNum[numOfAudiosPlayed.value]];
  isQuestionPlaying.value = true;
  const audio = playQuestion(question["Q"]);
  currentAudios.push(audio);
  
  // Set isQuestionPlaying to false when audio ends
  audio.onended = () => {
    isQuestionPlaying.value = false;
    
    // Reset cooldown after audio finishes
    setTimeout(() => {
      isRepeatButtonCooldown.value = false;
    }, 500); // Add a small additional cooldown after audio ends
  };
  
  // Backup timeout in case onended doesn't fire
  const estimatedDuration = question["Q"].length * 100 + 2000;
  setTimeout(() => {
    if (isQuestionPlaying.value) {
      isQuestionPlaying.value = false;
    }
    
    // Also reset cooldown here as a backup
    isRepeatButtonCooldown.value = false;
  }, estimatedDuration);
}
};

// Function for the record button - modified to handle press and hold
const startRecording = () => {
// Only start listening if:
// 1. We're not already listening
// 2. Haven't played all 5 questions
// 3. Intro isn't playing
// 4. Question isn't currently playing (this is the key change)
if (
  !isListening.value &&
  numOfAudiosPlayed.value < 5 &&
  !isIntroPlaying.value &&
  !isQuestionPlaying.value  // Only allow recording after question finishes
) {
  isListening.value = true;
  isRecording.value = true;
  
  startListening((transcript) => {
    const question = questionsDb[randQueNum[numOfAudiosPlayed.value]];
    console.log("User Answer:", transcript);
    transcription.value = transcript;
    
    if (transcript.trim().toLowerCase() === question["A"].toLowerCase()) {
      score.value++;
      playSound("correctaudio.mp3");
    } else {
      playSound("incorrectaudio.mp3");
    }
    
    stopListening();
    isListening.value = false;
    isRecording.value = false;
    numOfAudiosPlayed.value++;
    
    if (numOfAudiosPlayed.value < 5) {
      setTimeout(() => {
        playNextQuestion();
      }, 2000);
    } else {
      // Game Over
      setTimeout(() => {
        playScore(score.value);
      }, 2000);
    }
  });
}
};

// Function to stop recording when button is released
const stopRecording = () => {
if (isListening.value) {
  // Add a 1-second delay before stopping the recording
  setTimeout(() => {
    stopListening();
    isListening.value = false;
    isRecording.value = false;
  }, 1000);
}
};

onMounted(() => {
// Check device type on mount
checkDeviceType();

// Add resize listener for responsive behavior
window.addEventListener('resize', checkDeviceType);

// Request microphone access on page load
console.log("Requesting microphone access...");
requestMicPermission();
// Generate questions
generateQuestions();

watch(playButton, (newVal) => {
  if (newVal) {
    isIntroPlaying.value = true;
    const introAudio = playIntro("/spellingBee/spellingintro.mp3");
    currentAudios.push(introAudio);
    introAudio.onended = () => {
      isIntroPlaying.value = false;
      playNextQuestion();
    };
  }
});

// Remove keyboard event listeners since we're using buttons
});

onUnmounted(() => {
console.log("Navigated Back!");
stopAudios(currentAudios);
window.removeEventListener('resize', checkDeviceType);
});
</script>